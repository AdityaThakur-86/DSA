// CALCULATOR
#include<iostream>
using namespace std;
int main(){
    int a,b;  
    cout<<"Enter first number : ";
    cin>>a;
    cout<<"Enter second number : ";
    cin>>b;
    cout<<"The sum is : "<< (a+b)<< endl;  //ADDITION
    cout<<"The difference is :"<< (a-b)<< endl; //SUBSTRACTION
    cout<<"The product is :"<< (a*b)<< endl; //MULTIPLICATION
    cout<<"The quotient is :"<< (a/b)<< endl; //DIVISION
return 0;
}